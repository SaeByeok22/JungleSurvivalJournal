# 그리디 - 잃어버린 괄호 (백준 실버2)
# 문제 링크: https://www.acmicpc.net/problem/1541
# 사용자로부터 수식을 문자열로 입력받고, 양쪽 공백 제거
s = input().strip()

# '-' 기준으로 문자열을 나눔 → 최소값을 만들기 위한 핵심 단계
# 예: "55-50+40" → ['55', '50+40']
parts = s.split('-')

# 첫 번째 덩어리는 그대로 더해야 하므로 '+' 기준으로 나눠서 모두 더함
# 예: '55' → ['55'] → 55
answer = sum(map(int, parts[0].split('+')))

# 두 번째 덩어리부터는 전부 빼야 최소값이 됨
# 그래서 반복하면서 처리
for p in parts[1:]:
    # 각 덩어리를 '+' 기준으로 나눠서 숫자로 바꾼 뒤 전부 더함
    # 예: '50+40' → ['50','40'] → 90
    # 그리고 그 값을 결과에서 빼줌
    answer -= sum(map(int, p.split('+')))

# 최종 결과 출력
print(answer)